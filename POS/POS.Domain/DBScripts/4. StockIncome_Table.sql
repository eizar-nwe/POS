CREATE TABLE STOCKINCOME(
	ID				CHAR(36) NOT NULL,
	LINE_ID			INT IDENTITY(1,1) NOT NULL,
	SUPPLIER_ID		CHAR(36) NOT NULL,
	STOCKGRP_ID	CHAR(36) NOT NULL,
	STOCKITEM_ID	CHAR(36) NOT NULL,	
	PRICE			NUMERIC(18,2) NOT NULL,
	QUANTITY		NUMERIC(18,2) NOT NULL,
	REMARKS			NVARCHAR(255) NULL,
	CREATEDAT		DATETIME NOT NULL,
	CREATEDBY		CHAR(36) NOT NULL,
	UPDATEDAT		DATETIME NULL,
	UPDATEDBY		CHAR(36) NULL,
	IP				NVARCHAR(MAX) NOT NULL,
	ISACTIVE		BIT NOT NULL,
	PRIMARY KEY (ID, LINE_ID),
	CONSTRAINT FK_SUPR FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER(Id),
	CONSTRAINT FK_STKGROUP FOREIGN KEY (STOCKGRP_ID) REFERENCES STOCKGROUP(Id),
	CONSTRAINT FK_STKITEM FOREIGN KEY (STOCKITEM_ID) REFERENCES STOCKITEM(Id),	
)
