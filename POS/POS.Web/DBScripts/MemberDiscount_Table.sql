CREATE TABLE MEMBER_DISCOUNT(
	ID				CHAR(36) NOT NULL,
	LINE_ID			INT IDENTITY(1,1) NOT NULL,	
	MEMBER_ID		CHAR(36) NOT NULL,
	STOCKGRP_ID		CHAR(36) NOT NULL,
	STOCKITEM_ID	CHAR(36) NOT NULL,	
	ORIGIN_PRICE	NUMERIC(18,2) NOT NULL,
	OFFER_PRICE		NUMERIC(18,2) NOT NULL,
	DISC_TYPE		NVARCHAR(1),
	DISC_AMOUNT		NUMERIC(18,2),
	DISC_PERCENT	NUMERIC(18,2),
	FROM_DATE		DATETIME,
	TO_DATE			DATETIME,
	REMARKS			NVARCHAR(255) NULL,
	CREATEDAT		DATETIME NOT NULL,
	CREATEDBY		CHAR(36) NOT NULL,
	UPDATEDAT		DATETIME NULL,
	UPDATEDBY		CHAR(36) NULL,
	IP				NVARCHAR(MAX) NOT NULL,
	ISACTIVE		BIT NOT NULL,
	PRIMARY KEY (ID, LINE_ID),
	CONSTRAINT FK_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(Id),
	CONSTRAINT FK_STKGRP FOREIGN KEY (STOCKGRP_ID) REFERENCES STOCKGROUP(Id),
	CONSTRAINT FK_STKITM FOREIGN KEY (STOCKITEM_ID) REFERENCES STOCKITEM(Id),	
)